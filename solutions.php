    <h2 style="font-size: 36px; text-align: center;">Solutions to Optimize Your Electricity Consumption in Your Home</h2>

    <h2 style="font-size: 24px;">1. Switch off lights and electrical appliances when not using them</h2>
    <ul>
        <li>To save the most energy, switch off the power point rather than leave appliances on standby.</li>
        <li>Turn off your heater, cooling units and appliances when you go to bed or leave the house.</li>
        <li>Switch off your computer and equipment such as printers or Wi-Fi routers overnight or when you're away. Most computers have energy-saving settings that will turn the computer and screen off after a period of inactivity.</li>
    </ul>
    <h2 style="font-size: 24px;">2. Switch to energy-saving LED light globes</h2>
    <p>Energy-efficient globes could save up to 80% off your lighting costs. This is because LED bulbs use less power and last longer. That means you spend less money and time replacing them.<p>
    <h2 style="font-size: 24px;"></h2>
    <ul>
        <li>Shut doors to areas you're not using, and only cool or heat the rooms where you spend the most time.</li>
        <li>In cooler months, make sure your curtains or blinds seal your windows properly.</li>
        <li>Stop cool air leaking out by blocking draughts around doors and windows.</li>
        <li>In warmer months, keep your curtains closed during the day.</li>
        <li>External blinds or canvas awnings will also help keep your house cooler.</li>
    </ul>
    <h2 style="font-size: 24px;">4. Save energy in how you wash and dry clothes</h2>
    <ul>
        <li>Wait until your machine is full before starting a washing cycle.</li>
        <li>Washing clothes in cold water can save around $115 per year.</li>
        <li>You can also save by selecting the shortest appropriate washing cycle.</li>
        <li>Clothes dryers use lots of energy. Hang clothes outside to dry or use a fan to help dry them indoors.</li>
    </ul>
    <h2 style="font-size: 24px;">5. Save energy in the kitchen</h2>
    <h2 style="font-size: 18px;">Fridge</h2>
    <p>Your fridge runs 24/7 and is one of your most expensive appliances.<p>
    <ul>
        <li>The ideal fridge temperature for your freezer is 4 or 5 degrees Celsius and -15 to -18 degrees.</li>
        <li>Ensure the fridge door seal is tight and that no gaps or cracks let cold air escape.</li>
        <li>If you have a second fridge or freezer, only turn it on when needed.</li>
    </ul>
    <h2 style="font-size: 18px;">Cooking</h2>
    <ul>
        <li>Put frozen food in your fridge in the morning to thaw out and reduce cooking time in the evening.</li>
        <li>When cooking, use the microwave when you can. Microwaves use much less energy than an electric oven.</li>
        <li>Using a stove, keep lids on pots to reduce cooking time.</li>
        <li>Plan to cook more and have leftover meals for the next day or the whole week. Keep leftover meals in the freezer.</li>
    </ul>
    <h2 style="font-size: 18px;">Dishwasher</h2>
    <ul>
        <li>Use the economy cycle on your dishwasher, and only run it when it's full.</li>
    </ul>
